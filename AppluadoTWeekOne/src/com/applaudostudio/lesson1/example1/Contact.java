package com.applaudostudio.lesson1.example1;

public class Contact {
        String name;
        String email;
        String phoneNumber;

    public Contact() {
        this.name="";
        this.email="";
        this.phoneNumber="";
    }

    public Contact(String name, String email, String phoneNumber) {
        this.name = name;
        this.email = email;
        this.phoneNumber = phoneNumber;
    }
}
